        primary = scene["choices"][0]
        primary["text"] = refinement["primary"]
        primary["score"] = 80
        primary["feedback"] = refinement["primary_feedback"]
        scene["memory"] = refinement["memory"]
        followups_by_key = {item["key"]: item for item in refinement["followups"]}
        order = FOLLOWUP_ORDER_PATTERNS[scene_counter % len(FOLLOWUP_ORDER_PATTERNS)]
        scene["followups"] = [followups_by_key[key] for key in order]
        scene["recommended_followup"] = next(
            item["text"] for item in scene["followups"] if item["score"] == 20
        )
        scene_counter += 1


